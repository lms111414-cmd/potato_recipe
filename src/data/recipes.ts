// 유저 상태 기본값이 채워진 초기 레시피 목록 (LocalStorage가 없을 때의 기준)
export const recipes: Recipe[] = recipeSeeds.map(withUserState)

export const getRecipesByMood = (mood: Recipe['mood']): Recipe[] => {
  return recipes.filter((recipe) => recipe.mood === mood)
}

export const getRecipeById = (id: string): Recipe | undefined => {
  return recipes.find((recipe) => recipe.id === id)
}
